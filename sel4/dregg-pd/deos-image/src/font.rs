//! A COMPLETE, legible 8x8 bitmap font covering every printable ASCII glyph
//! (0x20 SPACE .. 0x7E '~'), with REAL descenders on g/j/p/q/y.
//!
//! This is the fix for the compositor-fb splash's broken 5x7 font (which dropped
//! 'g'/'4'/'f' shapes and had no descenders — "robigalia" rendered "robiɓalia").
//! Here every glyph is hand-laid as 8 rows, each row a u8 whose bits, MSB→LSB,
//! are the 8 pixels left→right (bit7 = leftmost). The glyph cell is 8 wide × 8
//! tall; we render at the top of the cell and let descenders use rows 6–7, so a
//! line of text with descenders reads correctly without clipping.
//!
//! The letterforms are an upright, slightly-rounded sans — chosen for legibility
//! at the integer pixel-zoom the screens use (scale 2–5). No external font crate
//! (the firmament's minimal-dep discipline): the bytes are right here.

/// One 8x8 glyph: 8 rows top→bottom; in each row bit7 is the leftmost pixel.
pub type Glyph = [u8; 8];

/// The blank cell (also the fallback for any unmapped byte).
const BLANK: Glyph = [0, 0, 0, 0, 0, 0, 0, 0];

