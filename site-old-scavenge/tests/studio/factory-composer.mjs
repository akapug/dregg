/**
 * Studio FACTORY AUTHORING smoke test — <dregg-factory-composer>.
 *
 * Verifies the factory tab end-to-end against the GENERATED data (no node,
 * no wasm needed):
 *   1. The tab mounts and loads all three catalogs (predicate / assurance /
 *      factory-samples) — failure renders an honest error, not a blank.
 *   2. PROOF BADGES come from data: the substrate-guarantee panel lists the
 *      assurance catalog's guarantees with their #assert_axioms pin counts
 *      and the kernel axiom triple, and the operator-discipline panel quotes
 *      the blueprint.rs expressibility limits.
 *   3. Loading the ESCROW worked example (generated by running the real Rust
 *      constructor) decodes its constraints into rows with enforcement badges.
 *   4. Composing a constraint updates the export, and the exported JSON has
 *      the FactoryDescriptor serde shape (state_constraints externally-tagged,
 *      32-byte arrays for Field values).
 *
 * Prereqs: dist served on :8080  →  npx serve dist -l 8080
 * Run:     node tests/studio/factory-composer.mjs
 * Env:     BASE (default http://localhost:8080)
 */

import { chromium } from '../../node_modules/playwright/index.mjs';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const BASE = process.env.BASE || 'http://localhost:8080';
const HERE = path.dirname(fileURLToPath(import.meta.url));
const ASSURANCE = JSON.parse(fs.readFileSync(
  path.join(HERE, '..', '..', 'src', '_includes', 'studio', 'assurance-catalog.generated.json'), 'utf8'));

let failures = 0;
function check(name, ok, detail = '') {
  console.log(`${ok ? 'PASS' : 'FAIL'}  ${name}${detail ? `  — ${detail}` : ''}`);
  if (!ok) failures += 1;
}

async function run() {
  const browser = await chromium.launch();
  const page = await browser.newPage();
  const errors = [];
  page.on('pageerror', (e) => errors.push(`pageerror: ${e.message}`));

  await page.goto(`${BASE}/studio.html#factory`, { waitUntil: 'domcontentloaded' });
  const composer = page.locator('dregg-factory-composer');
  await composer.waitFor({ state: 'attached', timeout: 20000 });
  await page.waitForFunction(() => {
    const el = document.querySelector('dregg-factory-composer');
    return el && /Author a factory/.test(el.textContent || '');
  }, { timeout: 20000 });
  check('factory composer mounts with catalogs', true);

  // 2. proof badges are DATA: every guarantee in the generated assurance
  // catalog appears, with its real pin count.
  for (const g of ASSURANCE.guarantees) {
    const found = await page.evaluate(({ title, pins }) => {
      const rows = [...document.querySelectorAll('dregg-factory-composer .dregg-fc__g')];
      return rows.some((r) =>
        (r.querySelector('.dregg-fc__g-name')?.textContent || '').includes(title) &&
        (r.querySelector('.dregg-fc__g-pins')?.textContent || '').includes(String(pins)));
    }, { title: g.title.split(' ')[0], pins: g.pins.length });
    check(`guarantee ${g.letter} (${g.title.slice(0, 24)}…) badge from catalog (${g.pins.length} pins)`, found);
  }
  const tripleShown = await page.evaluate((triple) => {
    const el = document.querySelector('dregg-factory-composer');
    return triple.every((t) => (el.textContent || '').includes(t));
  }, ASSURANCE.kernel_axiom_triple);
  check('kernel axiom triple quoted from catalog', tripleShown, ASSURANCE.kernel_axiom_triple.join(', '));

  const limitsShown = await page.evaluate(() => {
    const el = document.querySelector('dregg-factory-composer');
    return /balance.*sealed|CANNOT|discipline/i.test(el.textContent || '') &&
      el.querySelectorAll('.dregg-fc__limits li').length >= 1;
  });
  check('expressibility limits quoted from blueprint.rs', limitsShown);

  // 3. load the escrow worked example
  await page.click('dregg-factory-composer [data-example="escrow"]');
  await page.waitForFunction(() => {
    const el = document.querySelector('dregg-factory-composer');
    return el.querySelectorAll('.dregg-fc__entry').length >= 5;
  }, { timeout: 10000 });
  const entryCount = await page.locator('dregg-factory-composer .dregg-fc__entry').count();
  check('escrow example decodes into constraint rows', entryCount >= 5, `${entryCount} rows`);
  const enforcedBadges = await page.locator('dregg-factory-composer .dregg-fc__badge.is-enforced').count();
  check('per-constraint executor-enforced badges render', enforcedBadges >= entryCount - 2, `${enforcedBadges}`);
  const exNote = await page.evaluate(() => {
    const el = document.querySelector('dregg-factory-composer .dregg-fc__exnote');
    return el ? el.textContent : '';
  });
  check('worked-example provenance shown (descriptor hash)', /descriptor hash/i.test(exNote));

  // 4. exported JSON has the serde shape
  const exported = await page.evaluate(() => {
    const pre = document.querySelector('dregg-factory-composer .dregg-fc__json');
    try { return JSON.parse(pre.textContent); } catch { return null; }
  });
  check('export parses as JSON', !!exported);
  if (exported) {
    check('export has FactoryDescriptor fields',
      Array.isArray(exported.factory_vk) && exported.factory_vk.length === 32 &&
      'state_constraints' in exported && 'default_mode' in exported && 'allowed_cap_templates' in exported);
    const sc = exported.state_constraints || [];
    check('state_constraints externally tagged', sc.length >= 5 && sc.every((c) =>
      c && typeof c === 'object' && Object.keys(c).length === 1 && /^[A-Z]/.test(Object.keys(c)[0])));
    // Field values are 32-byte arrays (serde [u8;32]) somewhere in the tree.
    const json = JSON.stringify(sc);
    check('Field values are 32-byte arrays', /\[(0,){10,}/.test(json.replace(/\s/g, '')) || /"value":\[\d+(,\d+){31}\]/.test(json.replace(/\s/g, '')));
  }

  // 5. compose a fresh constraint on top and confirm the export grows
  const before = exported ? exported.state_constraints.length : 0;
  await page.selectOption('dregg-factory-composer .dregg-fc__add-kind', 'Immutable');
  await page.fill('dregg-factory-composer .dregg-fc__add-fields [data-field="index"]', '5');
  await page.click('dregg-factory-composer .dregg-fc__add-btn');
  await page.waitForTimeout(200);
  const after = await page.evaluate(() => {
    const pre = document.querySelector('dregg-factory-composer .dregg-fc__json');
    try { return JSON.parse(pre.textContent).state_constraints.length; } catch { return -1; }
  });
  check('adding a constraint grows the export', after === before + 1, `${before} → ${after}`);

  const fatal = errors.filter((e) => !/favicon|net::|Failed to fetch/i.test(e));
  check('no page errors', fatal.length === 0, fatal.join(' | '));

  await browser.close();
  console.log(failures ? `\n${failures} FAILURE(S)` : '\nALL PASS');
  process.exit(failures ? 1 : 0);
}

run().catch((e) => { console.error(e); process.exit(1); });
